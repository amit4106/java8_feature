System.out.println("Default Value of integer is "+in.i+"\n");
System.out.println("Default value of long is "+in.l+"\n");
System.out.println("Default value of float is "+in.f+"\n");
System.out.println("Default value of double is "+in.d+"\n");
System.out.println("Default value of char is "+in.c+"\n");
